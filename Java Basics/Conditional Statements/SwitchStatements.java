
public class SwitchStatements {
    public static void main(String[] args) {
        int number = 3;
        switch (number) {
            case 1:
                System.out.println("Samosa");
                break;
            case 2:
                System.out.println("Burger");
                break;
            case 3:
                System.out.println("Pizza");
                break;

            case 4:
                System.out.println("Pasta");
                break;

            default:
                System.out.println("No food");
        }
    }

}